# basic_operations.py

# define variables
number1 = 10
number2 = 5

# calculate
sum_result = number1 + number2
diff_result = number1 - number2   # number1 minus number2
prod_result = number1 * number2

# print results in the required format
print(f"Sum of {number1} and {number2} is {sum_result}")
print(f"Difference of {number1} and {number2} is {diff_result}")
print(f"Product of {number1} and {number2} is {prod_result}")
